// Export helper functions
export function getCertById(id) {
  return CERTIFICATIONS_EXPANDED.find(c => c.id === id)
}

export function getCertsByField(field) {
  const normalizedField = field.toLowerCase().replace(/ /g, '-')
  return CERTIFICATIONS_EXPANDED.filter(c => c.field === normalizedField)
}

export function getCertsByLevel(level) {
  return CERTIFICATIONS_EXPANDED.filter(c => c.level === level)
}

export function getCertsBySubfield(field, subfield) {
  return CERTIFICATIONS_EXPANDED.filter(c => c.field === field && c.subfield === subfield)
}

export function getTopCertsByDemand(field, limit = 5) {
  return getCertsByField(field)
    .sort((a, b) => b.demandScore - a.demandScore)
    .slice(0, limit)
}
